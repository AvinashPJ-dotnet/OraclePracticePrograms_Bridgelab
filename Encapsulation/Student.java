package Encapsulation;

public class Student {

	private String strName;
	private int iStudentRegistrationNo;
	private String strBatchName;
	public String getStrName() {
		return strName;
	}
	public void setStrName(String strName) {
		this.strName = strName;
	}
	public int getiStudentRegistrationNo() {
		return iStudentRegistrationNo;
	}
	public void setiStudentRegistrationNo(int iStudentRegistrationNo) {
		this.iStudentRegistrationNo = iStudentRegistrationNo;
	}
	public String getStrBatchName() {
		return strBatchName;
	}
	public void setStrBatchName(String strBatchName) {
		this.strBatchName = strBatchName;
	}
	
	
	
}
